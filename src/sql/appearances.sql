CREATE TABLE "appearances" (
    "ID" INTEGER NOT NULL,
    "yearID" SMALLINT NOT NULL,
    "teamID" CHARACTER(3) NOT NULL,
    "team_ID" INTEGER NULL,
    "lgID" CHARACTER(2) NULL,
    "playerID" VARCHAR(9) NOT NULL,
    "G_all" SMALLINT NULL,
    "GS" SMALLINT NULL,
    "G_batting" SMALLINT NULL,
    "G_defense" SMALLINT NULL,
    "G_p" SMALLINT NULL,
    "G_c" SMALLINT NULL,
    "G_1b" SMALLINT NULL,
    "G_2b" SMALLINT NULL,
    "G_3b" SMALLINT NULL,
    "G_ss" SMALLINT NULL,
    "G_lf" SMALLINT NULL,
    "G_cf" SMALLINT NULL,
    "G_rf" SMALLINT NULL,
    "G_of" SMALLINT NULL,
    "G_dh" SMALLINT NULL,
    "G_ph" SMALLINT NULL,
    "G_pr" SMALLINT NULL,
    PRIMARY KEY ("ID"),
    FOREIGN KEY("team_ID") REFERENCES "teams" ("ID") ON UPDATE NO ACTION ON DELETE NO ACTION,
    FOREIGN KEY("playerID") REFERENCES "people" ("playerID") ON UPDATE NO ACTION ON DELETE NO ACTION
);